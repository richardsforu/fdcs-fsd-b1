class SwitchDemo{

    
    public static void main(String args[]){

   
         int n=200;

         switch(n){ // switched block
          case 1:
             System.out.println("Value is 1");
             break;
          case 20:
             System.out.println("Value is 20");
             break;
          case 3:
            System.out.println("Value is 3");
             break;
          default:
            System.out.println("No case value matches. try again...");
            
         } // end of switch
        
       

    }
}