package com;

public class Notes {
	
	/*
	 * Access Modifiers
	 * ------------------
	 * 	-> public
	 * 	-> default
	 * 	-> protected
	 * 	-> private
	 * 
	 * public
	 * -------
	 * 	-> public classes and public data is available to all 
	 *     the packages in the same application
	 *
	 *  
	 * default
	 * -------
	 * -> Default classes, properties and methods are available
	 *    to the classes with in the same package. Other package
	 *    classes can not use default classes and data
	 *    
	 * protected
	 * ----------
	 *  -> protected classes, properties and methods are
	 *     available to the all the classes with in the same
	 *     package, and sub classes of other packages
	 *     
	 *  
	 *  
	 *  
	 *  
	 *  
	 */
	

}
